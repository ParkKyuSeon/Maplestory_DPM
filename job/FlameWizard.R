        }
        DC2$Time[(nrow(DC2)-1)] <- DC2$Time[(nrow(DC2)-1)] + 360
        DC2$Time[nrow(DC2)] <- DC2$Time[(nrow(DC2)-1)] + 150
        DC2 <- DC2Control(DC2)
      }
    }
    
    if(k != length(BuffList)) {
      for(i in 1:length(BuffList[[k]])) {
        if(sum(rownames(BuffFinal)==BuffList[[k]][i]) > 0) {
